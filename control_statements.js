
//Looping
//ForLoop
sum=0;
for(i=0;i<10;i++)
{
    sum+=i;
}
console.log(sum)

//WhileLoop
sum=0
i=0
while(i<10)
{
    sum+=i
}
console.log(sum)

//doWhileLoop
sum=0
i=0
do{
    sum+=i
    i++
}
while(i<10)
console.log(sum)


